package com.bean;

public class Crime_Details {
	
	private int casenumber;
	private int premisecode;
	private String premiseDesc;
	private int weaponused;
	private String weapondesc;
	public int getCasenumber() {
		return casenumber;
	}
	public void setCasenumber(int casenumber) {
		this.casenumber = casenumber;
	}
	public int getPremisecode() {
		return premisecode;
	}
	public void setPremisecode(int premisecode) {
		this.premisecode = premisecode;
	}
	public String getPremiseDesc() {
		return premiseDesc;
	}
	public void setPremiseDesc(String premiseDesc) {
		this.premiseDesc = premiseDesc;
	}
	public int getWeaponused() {
		return weaponused;
	}
	public void setWeaponused(int weaponused) {
		this.weaponused = weaponused;
	}
	public String getWeapondesc() {
		return weapondesc;
	}
	public void setWeapondesc(String weapondesc) {
		this.weapondesc = weapondesc;
	}
	
	

}
