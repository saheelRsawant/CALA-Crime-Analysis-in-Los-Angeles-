package com.bean;

public class Case_Record {
	
	private int casenumber;
	private String dateoccurred;
	private int timeOccurred;
	private String datereported;
	private String statusdesc;
	private int reportingdistrict;
	public int getCasenumber() {
		return casenumber;
	}
	public void setCasenumber(int casenumber) {
		this.casenumber = casenumber;
	}
	public String getDateoccurred() {
		return dateoccurred;
	}
	public void setDateoccurred(String dateoccurred) {
		this.dateoccurred = dateoccurred;
	}
	public int getTimeOccurred() {
		return timeOccurred;
	}
	public void setTimeOccurred(int timeOccurred) {
		this.timeOccurred = timeOccurred;
	}
	public String getDatereported() {
		return datereported;
	}
	public void setDatereported(String datereported) {
		this.datereported = datereported;
	}
	public String getStatusdesc() {
		return statusdesc;
	}
	public void setStatusdesc(String statusdesc) {
		this.statusdesc = statusdesc;
	}
	public int getReportingdistrict() {
		return reportingdistrict;
	}
	public void setReportingdistrict(int reportingdistrict) {
		this.reportingdistrict = reportingdistrict;
	}
	
	
}